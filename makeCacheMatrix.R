makeCacheMatrix <- function(x = numeric()) {
  ## This function creates a special "matrix" object 
  ## that can cache its inverse.
  
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setmean <- function(mean) m <<- mean
  getmean <- function() m
  list(set = set, get = get,
       setmean = setmean,
       getmean = getmean)
}